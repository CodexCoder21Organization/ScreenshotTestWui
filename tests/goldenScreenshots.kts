/**
 * Golden-screenshot test for the ScreenshotTest WUI — the WUI dogfooding the very service it renders
 * (workstream:
 * https://github.com/CodexCoder21Organization/PlanRepository/blob/main/workstreams/ScreenshotTest.md).
 *
 * It launches [ScreenshotFixtureServer] (a deterministic, frozen-clock ScreenshotTest WUI serving a
 * fixed set of sessions with in-memory PNGs) on a hosted worker via the version-pinned renderer,
 * captures the sessions list and a compare session's detail page (plus the results-table crop), and
 * compares each capture against a committed golden. A pixel difference beyond tolerance, or a missing
 * golden, fails the build.
 *
 * ## Two modes
 *
 *  - **compare** (default): uploads the committed `screenshots` PNGs as goldens, renders, and asserts
 *    every verdict is `MATCH`. This is what runs in CI. Provisions a droplet by design.
 *  - **record** (`SCREENSHOTTEST_RECORD=1`): renders without goldens and downloads the freshly
 *    captured PNGs into `screenshots/`, plus `screenshots/RENDERER_VERSION`. Run this to (re)generate
 *    goldens after an intentional visual change, then inspect and commit the PNGs.
 */
@build.kotlin.annotations.Timeout(3000)
fun goldenScreenshots() {
    fun log(msg: String) = println("[golden] $msg")

    val record = System.getenv("SCREENSHOTTEST_RECORD") == "1"
    val chunkSize = 1_048_576 // 1 MiB — the service's per-chunk upload limit.

    // Locate the ScreenshotTestWui repo root (the dir holding build.kts + tests/) so the committed
    // goldens under <root>/screenshots are found regardless of the process working directory.
    fun looksLikeRepoRoot(d: File): Boolean = File(d, "build.kts").isFile && File(d, "tests").isDirectory
    val userDir = File(System.getProperty("user.dir")).absoluteFile
    fun searchDown(dir: File, depth: Int): File? {
        if (depth < 0) return null
        val skip = setOf("tools", "maven", "node_modules", "browsers", "buildtest-data", "coursier", "jars")
        val children = dir.listFiles { f -> f.isDirectory && !f.name.startsWith(".") && f.name !in skip }
            ?: return null
        for (child in children.sortedBy { it.name }) {
            if (looksLikeRepoRoot(child)) return child
            searchDown(child, depth - 1)?.let { return it }
        }
        return null
    }
    val repoRoot = generateSequence(userDir) { it.parentFile }.firstOrNull(::looksLikeRepoRoot)
        ?: searchDown(userDir, 4)
        ?: userDir
    val screenshotsDir = File(repoRoot, "screenshots")
    val rendererVersionFile = File(screenshotsDir, "RENDERER_VERSION")
    log("repo root = ${repoRoot.absolutePath}; screenshots = ${screenshotsDir.absolutePath}")

    fun sha256Hex(bytes: ByteArray): String =
        MessageDigest.getInstance("SHA-256").digest(bytes).joinToString("") { "%02x".format(it.toInt() and 0xff) }

    fun zipDirectory(dir: File): ByteArray {
        val out = ByteArrayOutputStream()
        ZipOutputStream(out).use { zip ->
            dir.walkTopDown().filter { it.isFile }.sortedBy { it.relativeTo(dir).invariantSeparatorsPath }.forEach { file ->
                zip.putNextEntry(ZipEntry(file.relativeTo(dir).invariantSeparatorsPath))
                file.inputStream().use { it.copyTo(zip) }
                zip.closeEntry()
            }
        }
        return out.toByteArray()
    }

    fun classpathUpload(entry: File, index: Int): Pair<ByteArray, String>? = when {
        entry.isFile && entry.name.endsWith(".jar") -> entry.readBytes() to "%05d-%s".format(index, entry.name)
        entry.isFile && entry.name.endsWith(".zip") -> entry.readBytes() to "%05d-%s.jar".format(index, entry.name.removeSuffix(".zip"))
        entry.isDirectory -> {
            val safe = entry.name.ifBlank { "classes" }.replace(Regex("[^A-Za-z0-9._-]"), "_")
            zipDirectory(entry) to "%05d-%s.jar".format(index, safe)
        }
        else -> null
    }

    fun uploadFile(api: ScreenshotTestApi, sessionId: String, role: String, fileName: String, bytes: ByteArray) {
        if (bytes.isEmpty()) return
        var index = 0
        var offset = 0
        while (offset < bytes.size) {
            val end = minOf(offset + chunkSize, bytes.size)
            api.uploadFileChunk(sessionId, role, fileName, index, bytes.copyOfRange(offset, end))
            index++
            offset = end
        }
        api.finalizeFile(sessionId, role, fileName, index, sha256Hex(bytes))
    }

    fun downloadImage(api: ScreenshotTestApi, sessionId: String, key: String, kind: String): ByteArray {
        val out = ByteArrayOutputStream()
        var offset = 0L
        while (true) {
            val slice = api.getImageChunk(sessionId, key, kind, offset, chunkSize) ?: break
            if (slice.isEmpty()) break
            out.write(slice)
            offset += slice.size
        }
        return out.toByteArray()
    }

    // The scenarios to capture: the sessions list (full page) and a COMPLETED compare session's detail
    // page (full page) plus the results-table crop. 1280x900 viewport. waitSelector holds the capture
    // until the page's marker element is present.
    fun buildScenariosJson(): String {
        val scenarios = JSONArray()
        scenarios.put(
            JSONObject()
                .put("name", "sessions-list")
                .put("path", "/")
                .put("waitSelector", ".date-relative")
                .put("settleMs", 800)
                .put("viewportWidth", 1280)
                .put("viewportHeight", 900)
                .put("fullPage", true)
        )
        val components = JSONArray()
            .put(JSONObject().put("name", "results-table").put("selector", "#results-table"))
        scenarios.put(
            JSONObject()
                .put("name", "session-detail")
                .put("path", "/session?id=sess-a1b2c3d4")
                .put("waitSelector", "#results-table")
                .put("settleMs", 800)
                .put("viewportWidth", 1280)
                .put("viewportHeight", 900)
                .put("fullPage", true)
                .put("components", components)
        )
        return JSONObject().put("scenarios", scenarios).toString()
    }

    // ContainerNursery libp2p bootstrap peer — the entry point to the P2P network where
    // url://screenshottest/ is registered (same peer the real-droplet e2e tests use).
    val cnPeerId = "12D3KooWLMyXNfwhcX1YsiNx3hnjk3GGSfsU1fydRa8bzrE6scMT"
    val cnMultiaddr = "/ip4/198.199.106.165/tcp/35000/p2p/$cnPeerId"
    val cnBootstrapPeer = Libp2pPeer.remote(
        peerId = cnPeerId,
        multiaddresses = listOf(cnMultiaddr),
        advertisedServices = listOf("screenshottest"),
    )

    val resolver = UrlResolver(UrlProtocol2(bootstrapPeers = listOf(cnBootstrapPeer)))
    try {
        val api = resolver.openSandboxedConnection(
            "url://screenshottest/",
            ScreenshotTestApi::class,
            connectionTimeoutMs = 180_000,
            methodInvocationTimeoutSeconds = 300,
        )

        val rendererVersion = api.getRendererVersion()
        log("service renderer version = $rendererVersion")
        assertTrue(rendererVersion.isNotBlank(), "getRendererVersion() must return a non-blank renderer identity")

        // Renderer-version handshake (compare mode only): the committed goldens are only valid for the
        // renderer that produced them.
        if (!record) {
            assertTrue(
                rendererVersionFile.isFile,
                "screenshots/RENDERER_VERSION is missing at ${rendererVersionFile.absolutePath}. " +
                    "Record goldens first: SCREENSHOTTEST_RECORD=1 scripts/test.bash --test tests/goldenScreenshots.kts",
            )
            val recordedRenderer = rendererVersionFile.readText().trim()
            assertEquals(
                recordedRenderer, rendererVersion,
                "Golden renderer mismatch: the committed goldens were recorded by renderer " +
                    "'$recordedRenderer', but url://screenshottest/ now reports '$rendererVersion'. " +
                    "Regenerate the goldens: SCREENSHOTTEST_RECORD=1 scripts/test.bash --test tests/goldenScreenshots.kts",
            )
        }

        val classpathEntries = System.getProperty("java.class.path")
            .split(File.pathSeparator)
            .filter { it.isNotBlank() }
        log("current JVM classpath has ${classpathEntries.size} entries")

        val mode = if (record) "record" else "compare"
        val sessionId = api.createSession(
            "ScreenshotTestWui golden screenshots",
            mode,
            "screenshottest.wui.ScreenshotFixtureServerKt",
            buildScenariosJson(),
        )
        log("created session $sessionId (mode=$mode)")

        try {
            var uploaded = 0
            classpathEntries.forEachIndexed { index, path ->
                val prepared = classpathUpload(File(path), index) ?: return@forEachIndexed
                uploadFile(api, sessionId, "classpath", prepared.second, prepared.first)
                uploaded++
            }
            assertTrue(uploaded > 0, "No classpath entries were uploaded — the child WUI cannot be launched")
            log("uploaded $uploaded classpath entries")

            if (!record) {
                val goldens = (screenshotsDir.listFiles { f -> f.isFile && f.name.endsWith(".png") } ?: emptyArray())
                    .sortedBy { it.name }
                assertTrue(
                    goldens.isNotEmpty(),
                    "No golden PNGs found under ${screenshotsDir.absolutePath}. " +
                        "Record them first: SCREENSHOTTEST_RECORD=1 scripts/test.bash --test tests/goldenScreenshots.kts",
                )
                for (golden in goldens) uploadFile(api, sessionId, "golden", golden.name, golden.readBytes())
                log("uploaded ${goldens.size} goldens")
            }

            api.startRender(sessionId)
            log("render started; polling every 10s...")

            val deadline = System.currentTimeMillis() + 30 * 60 * 1000L
            var state = "PENDING"
            var error: String? = null
            while (System.currentTimeMillis() < deadline) {
                val status = JSONObject(api.getSessionStatus(sessionId))
                state = status.getString("state")
                error = if (status.has("error") && !status.isNull("error")) status.getString("error") else null
                log("session state = $state")
                if (state == "COMPLETED" || state == "FAILED") break
                Thread.sleep(10_000)
            }
            assertEquals(
                "COMPLETED", state,
                "Render did not complete: final state=$state" + (error?.let { ", error=$it" } ?: "") +
                    ". Session=$sessionId, renderer=$rendererVersion.",
            )

            val results = JSONObject(api.getResultsJson(sessionId))
            val resultsRenderer = results.optString("rendererVersion", "")
            assertEquals(
                rendererVersion, resultsRenderer,
                "results.json rendererVersion ('$resultsRenderer') disagrees with getRendererVersion() ('$rendererVersion')",
            )
            val keyResults = results.getJSONArray("results")
            assertTrue(keyResults.length() > 0, "results.json contained no per-key results")

            if (record) {
                screenshotsDir.mkdirs()
                val summary = StringBuilder()
                for (i in 0 until keyResults.length()) {
                    val r = keyResults.getJSONObject(i)
                    val key = r.getString("key")
                    val verdict = r.getString("verdict")
                    val image = downloadImage(api, sessionId, key, "actual")
                    assertTrue(image.isNotEmpty(), "The service returned no 'actual' image bytes for key '$key' (verdict=$verdict)")
                    File(screenshotsDir, "$key.png").writeBytes(image)
                    summary.appendLine("  screenshots/$key.png  (${image.size} bytes, verdict=$verdict)")
                }
                rendererVersionFile.writeText(rendererVersion + "\n")
                log("RECORDED ${keyResults.length()} goldens:\n$summary")
                log("Wrote ${rendererVersionFile.name} = $rendererVersion")
                log("Inspect the PNGs under ${screenshotsDir.absolutePath}, then commit the screenshots/ directory.")
            } else {
                val failures = StringBuilder()
                for (i in 0 until keyResults.length()) {
                    val r = keyResults.getJSONObject(i)
                    val verdict = r.getString("verdict")
                    if (verdict != "MATCH") {
                        failures.appendLine(
                            "  key='${r.getString("key")}' verdict=$verdict" +
                                " diffPixelCount=${r.optInt("diffPixelCount", -1)}" +
                                " maxChannelDelta=${r.optInt("maxChannelDelta", -1)}" +
                                " totalPixels=${r.optInt("totalPixels", -1)}" +
                                " golden=${r.optInt("goldenWidth", -1)}x${r.optInt("goldenHeight", -1)}" +
                                " actual=${r.optInt("actualWidth", -1)}x${r.optInt("actualHeight", -1)}"
                        )
                    }
                }
                if (failures.isNotEmpty()) {
                    fail(
                        "Golden screenshot comparison FAILED (renderer=$rendererVersion):\n$failures" +
                            "\nIf this visual change is intentional, regenerate and commit the goldens:\n" +
                            "  SCREENSHOTTEST_RECORD=1 scripts/test.bash --test tests/goldenScreenshots.kts\n" +
                            "then inspect the diff heatmaps and the new screenshots/*.png before committing."
                    )
                }
                log("All ${keyResults.length()} screenshot keys MATCH.")
            }
        } finally {
            try { api.deleteSession(sessionId) } catch (e: Exception) { log("deleteSession($sessionId) failed during cleanup: ${e.message}") }
        }
    } finally {
        try { resolver.close() } catch (e: Exception) { log("resolver.close() failed during cleanup: ${e.message}") }
    }
}
